//! Node modules committed rule.
//!
//! Checks if node_modules directory is tracked in git.

use crate::db::models::{ProjectWithStatus, Scope};
use crate::db::Database;
use crate::services::diagnostics::models::{DiagnosticIssue, RuleMetadata, Severity};
use crate::services::diagnostics::rules::{rule_metadata, DiagnosticRule};
use std::process::Command;

pub struct NodeModulesCommittedRule;

impl DiagnosticRule for NodeModulesCommittedRule {
    fn metadata(&self) -> RuleMetadata {
        rule_metadata(
            "security/node-modules-committed",
            "node_modules Committed",
            "node_modules directory appears to be tracked in git",
            false, // Opt-in
            Severity::Error,
            None,
            false,
        )
    }

    fn check(
        &self,
        _db: &Database,
        scope: &Scope,
        projects: &[ProjectWithStatus],
    ) -> Result<Vec<DiagnosticIssue>, String> {
        let mut issues = Vec::new();

        for project in projects {
            // Skip temp projects
            if project.project.is_temp {
                continue;
            }

            // Check if node_modules is tracked using git ls-files
            let output = Command::new("git")
                .args(["ls-files", "node_modules", "--error-unmatch"])
                .current_dir(&project.project.path)
                .output();

            if let Ok(output) = output {
                // If exit code is 0, files were found
                if output.status.success() {
                    issues.push(DiagnosticIssue::new(
                        scope.id.clone(),
                        Some(project.project.id.clone()),
                        "security/node-modules-committed".to_string(),
                        Severity::Error,
                        "node_modules Committed".to_string(),
                        format!(
                            "Project '{}' has node_modules tracked in git",
                            project.project.name
                        ),
                    ));
                }
            }
        }

        Ok(issues)
    }
}
